package com.nitro.platform.vo;

import java.util.List;

public class Files {

	// List of tags per file.
	private List<Tag> tags;
	private String fileName;
	
	public Files(){
	}
	
	public List<Tag> getTags() {
		return tags;
	}

	public void setTags(List<Tag> tags) {
		this.tags = tags;
	}

	public String getFileName() {
		return fileName;
	}
	
	
	
	
	
}
